def check_python_version():
    if sys.version_info[:2] != (3, 12):
        print("Công cụ yêu cầu Python 3.12")
        print(f"Phiên bản hiện tại: {sys.version.split()[0]}")
        sys.exit(1)


SYMBOLS = ["Δ", "λ", "Ω", "τ", "σ", "Φ", "Ψ", "μ", "κ", "π", "θ", "ξ", "ζ", "ℏ", "∇"]


def random_symbol():
    return random.choice(SYMBOLS) + "".join(random.choice(string.digits) for _ in range(3))


def show_boot_screen():
    os.system("cls" if os.name == "nt" else "clear")
    print(ASCII_BANNER)
    print("Đang khởi tạo", end="")
    for _ in range(5):
        time.sleep(0.2)
        print(".", end="", flush=True)
    print("\n")


def encode_multi_layer(data):
    layers = random.randint(2, 6)
    for _ in range(layers):
        data = marshal.dumps(data)
        data = zlib.compress(data)
        data = base64.b64encode(data)
    return data.decode(), layers


def build_decode_expression(var_name, layers):
    expr = var_name
    for _ in range(layers):
        expr = (
            "marshal.loads("
            "__import__('zlib').decompress("
            "__import__('base64').b64decode("
            f"{expr}"
            ")))"
        )
    return expr


def build_loader_style_one(encoded_blob, layers):
    core = random_symbol()
    executor = random_symbol()
    arg = random_symbol()

    decode_expr = build_decode_expression(arg, layers)

    return f"""import marshal,base64,zlib

{core}=lambda {arg}:{decode_expr}
{executor}=lambda x:exec({core}(x))

{executor}('{encoded_blob}')
"""


def build_loader_style_two(encoded_blob, layers):
    decoder = random_symbol()
    runner = random_symbol()
    param = random_symbol()

    decode_expr = build_decode_expression(param, layers)

    return f"""import marshal,base64,zlib

def {decoder}({param}):
    return {decode_expr}

def {runner}(blob):
    exec({decoder}(blob))

{runner}('{encoded_blob}')
"""


def build_loader(encoded_blob, layers):
    style = random.randint(1, 2)
    if style == 1:
        return build_loader_style_one(encoded_blob, layers)
    return build_loader_style_two(encoded_blob, layers)


def obfuscate_source(source_code):
    compiled = compile(source_code, "<obfuscated>", "exec")
    dumped = marshal.dumps(compiled)
    encoded_blob, layers = encode_multi_layer(dumped)
    return build_loader(encoded_blob, layers).strip() + "\n"


def main():
    check_python_version()
    show_boot_screen()

    print(ASCII_INPUT)

    input_file = input("File đầu vào  : ").strip()
    output_file = input("File đầu ra   : ").strip()

    if not os.path.isfile(input_file):
        print("Không tìm thấy file đầu vào.")
        return

    try:
        with open(input_file, "r", encoding="utf-8") as f:
            source = f.read()

        result = obfuscate_source(source)

        with open(output_file, "w", encoding="utf-8") as f:
            f.write(result)

        print("\nHoàn tất.")
        print(f"Đã lưu tại: {output_file}")

    except Exception as e:
        print(f"Lỗi: {e}")


if __name__ == "__main__":
    main()
